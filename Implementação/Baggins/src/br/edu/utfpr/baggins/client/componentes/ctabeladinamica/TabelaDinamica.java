package br.edu.utfpr.baggins.client.componentes.ctabeladinamica;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.FIELD)
public @interface TabelaDinamica
{
	public String nomeCampo() default "";

	public boolean visivel() default true;

	public boolean grid() default false;
	
	public String whereComboBox() default "";

	public String comboBoxVariavel() default "";

	
}